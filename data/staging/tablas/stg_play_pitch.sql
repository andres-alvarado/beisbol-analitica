DROP TABLE stg_play_pitch;

CREATE TABLE IF NOT EXISTS stg_play_pitch (
  callCode VARCHAR(100),
  callDescription VARCHAR(100),
  description VARCHAR(700),
  code VARCHAR(100),
  ballColor VARCHAR(100),
  trailColor VARCHAR(100),
  isInPlay TINYINT,
  isStrike TINYINT,
  isBall TINYINT,
  typeCode VARCHAR(100),
  typeDescription VARCHAR(100),
  hasReview TINYINT,
  runnerGoing TINYINT,
  balls INTEGER,
  strikes INTEGER,
  startSpeed VARCHAR(100),
  endSpeed VARCHAR(100),
  strikeZoneTop DOUBLE,
  strikeZoneBottom DOUBLE,
  aY VARCHAR(100),
  aZ VARCHAR(100),
  pfxX VARCHAR(100),
  pfxZ VARCHAR(100),
  pX VARCHAR(100),
  pZ VARCHAR(100),
  vX0 VARCHAR(100),
  vY0 VARCHAR(100),
  vZ0 VARCHAR(100),
  x DOUBLE,
  y DOUBLE,
  x0 VARCHAR(100),
  y0 VARCHAR(100),
  z0 VARCHAR(100),
  aX VARCHAR(100),
  zone VARCHAR(100),
  typeConfidence VARCHAR(100),
  breakAngle VARCHAR(100),
  breakLength VARCHAR(100),
  breakY VARCHAR(100),
  spinRate VARCHAR(100),
  spinDirection VARCHAR(100),
  launchSpeed VARCHAR(100),
  launchAngle VARCHAR(100),
  totalDistance VARCHAR(100),
  trajectory VARCHAR(100),
  hardness VARCHAR(100),
  location VARCHAR(100),
  coordX DOUBLE,
  coordY DOUBLE,
  atBatIndex INTEGER,
  gamePk INTEGER,
  `index` INTEGER,
  pfxId VARCHAR(100),
  playId VARCHAR(100),
  pitchNumber INTEGER,
  startTime VARCHAR(100),
  endTime VARCHAR(100),
  isPitch TINYINT,
  type VARCHAR(100)
) ENGINE = INNODB;
